<h1>First Line</h1>

<p>I know that I shall meet my fate</p>
<p>Somewhere among the clouds above;</p>
<p>Those that I fight I do not hate,</p>
<p>Those that I guard I do not love;</p>
<p>My country is Kiltartan Cross,</p>
<p>My countrymen Kiltartan’s poor,</p>
<p>No likely end could bring them loss</p>
<p>Or leave them happier than before.</p>
<p>Nor law, nor duty bade me fight,</p>
<p>Nor public men, nor cheering crowds,</p>
<p>A lonely impulse of delight</p>
<p>Drove to this tumult in the clouds;</p>
<p>I balanced all, brought all to mind,</p>
<p>The years to come seemed waste of breath,</p>
<p>A waste of breath the years behind</p>
<p>In balance with this life, this death.</p>

